"""
Pitch Generation Workflow

LangGraph StateGraph that orchestrates end-to-end project pitch generation
from a single prompt. Chains multiple CrewAI crews and optionally generates
a Gamma pitch deck.

Graph:
    [START] → parse_prompt → concept_crew → strategy_crew → positioning_crew
           → assemble_project → route_deck
                                 ├→ generate_deck (if requested)
                                 └→ skip_deck
           → finalize → [END]

Split endpoints allow using individual phases:
    - /pitch/generate: Full end-to-end pipeline
    - /pitch/enhance: Start from partial data, fill gaps
    - /pitch/deck: Generate deck from existing project data
"""

from __future__ import annotations

from typing import Any, Literal

from ..core.graph_builder import GraphBuilder
from ..core.state import PitchGenerationState


# ── Node Functions ──────────────────────────────────────────────────────


async def parse_prompt(state: PitchGenerationState) -> dict[str, Any]:
    """Parse and normalize the input prompt.

    For enhance mode, merges partial_data into state so downstream
    crews can skip already-filled fields.
    """
    partial = state.get("partial_data") or {}
    updates: dict[str, Any] = {}

    # Carry forward any pre-filled fields from partial data
    for field in [
        "title", "problem_statement", "solution", "categories",
        "project_type", "duration", "milestones", "resources",
        "tags", "target_audience", "expected_impact", "description",
    ]:
        if field in partial and partial[field]:
            updates[field] = partial[field]

    return updates


