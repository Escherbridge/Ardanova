async def assemble_project(state: PitchGenerationState) -> dict[str, Any]:
    """Assemble all crew outputs into the final project data structure.

    This produces the complete project payload that matches the
    .NET backend CreateProjectDto format.
    """
    project_data = {
        "title": state.get("title", ""),
        "description": state.get("description", ""),
        "problemStatement": state.get("problem_statement", ""),
        "solution": state.get("solution", ""),
        "categories": state.get("categories", []),
        "projectType": state.get("project_type", "PRODUCT"),
        "duration": state.get("duration"),
        "targetAudience": state.get("target_audience"),
        "expectedImpact": state.get("expected_impact"),
        "timeline": state.get("timeline"),
        "tags": ", ".join(state.get("tags", [])),
        "milestones": state.get("milestones", []),
        "resources": state.get("resources", []),
    }

    return {"project_data": project_data}


def route_deck(
    state: PitchGenerationState,
) -> Literal["generate_deck", "skip_deck"]:
    """Route: generate a Gamma pitch deck or skip."""
    return "generate_deck" if state.get("generate_deck") else "skip_deck"


async def generate_deck(state: PitchGenerationState) -> dict[str, Any]:
    """Generate a Gamma pitch deck from the assembled project data.

    Uses the Gamma API to create a presentation. Returns the URL
    if successful, or a message if the API key is not configured.
    """
    # TODO: Use GammaGenerateTool when API key is configured
    # Build a rich prompt from the project data for Gamma
    project = state.get("project_data", {})
    title = project.get("title", "Untitled")

    # Compose pitch content for Gamma
    pitch_content = f"""
Project: {title}

Problem:
{project.get('problemStatement', '')}

Solution:
{project.get('solution', '')}

Target Audience:
{project.get('targetAudience', '')}

Expected Impact:
{project.get('expectedImpact', '')}

Milestones:
"""
    for m in project.get("milestones", []):
        pitch_content += f"- {m.get('title', '')}: {m.get('description', '')} (Target: {m.get('target_date', '')})\n"

    pitch_content += "\nResources Needed:\n"
    for r in project.get("resources", []):
        cost = r.get("estimated_cost", 0)
        pitch_content += f"- {r.get('name', '')}: ${cost:,.0f}\n"

    return {
        "deck_url": "",
        "deck_generation_id": "",
        # The pitch_content would be passed to GammaGenerateTool._run()
        # Stub response until API key is configured:
        "result": {
            **_build_result(state),
            "deck": {
                "status": "pending_configuration",
                "message": "Gamma API key not configured. Set GAMMA_API_KEY to enable.",
                "pitch_content_preview": pitch_content[:500],
            },
        },
    }


async def skip_deck(state: PitchGenerationState) -> dict[str, Any]:
    """Skip deck generation, just pass through."""
    return {}


async def finalize(state: PitchGenerationState) -> dict[str, Any]:
    """Finalize the pitch generation result.

    If result was already set by generate_deck, just return.
    Otherwise, build the result without deck info.
    """
    if state.get("result"):
        return {}

    return {"result": _build_result(state)}


def _build_result(state: PitchGenerationState) -> dict[str, Any]:
    """Build the API response from the current state."""
    project = state.get("project_data", {})
    return {
        "project": project,
        "metadata": {
            "prompt": state.get("prompt", ""),
            "phases_completed": [
                "concept",
                "strategy",
                "positioning",
                "assembly",
            ],
        },
    }


# ── Graph Construction ──────────────────────────────────────────────────


def build_pitch_generation_graph() -> Any:
    """Build and compile the pitch generation LangGraph workflow.

    Returns:
        Compiled StateGraph supporting ainvoke() and astream().
    """
    graph = (
        GraphBuilder(PitchGenerationState)
        .add_function_node("parse_prompt", parse_prompt)
        .add_function_node("concept_crew", run_concept_crew)
        .add_function_node("strategy_crew", run_strategy_crew)
        .add_function_node("positioning_crew", run_positioning_crew)
        .add_function_node("assemble", assemble_project)
        .add_function_node("generate_deck", generate_deck)
        .add_function_node("skip_deck", skip_deck)
        .add_function_node("finalize", finalize)
        # Linear flow through crews
        .set_entry("parse_prompt")
        .add_edge("parse_prompt", "concept_crew")
        .add_edge("concept_crew", "strategy_crew")
        .add_edge("strategy_crew", "positioning_crew")
        .add_edge("positioning_crew", "assemble")
        # Conditional deck generation
        .add_router("assemble", route_deck, {
            "generate_deck": "generate_deck",
            "skip_deck": "skip_deck",
        })
        .add_edge("generate_deck", "finalize")
        .add_edge("skip_deck", "finalize")
        .add_terminal("finalize")
        .build()
    )

    return graph


# Compile at module level
pitch_generation_graph = build_pitch_generation_graph()


# ── Deck-only Graph ────────────────────────────────────────────────────


def build_deck_only_graph() -> Any:
    """Build a minimal graph just for generating a deck from existing project data.

    Used by the /pitch/deck endpoint.
    """
    graph = (
        GraphBuilder(PitchGenerationState)
        .add_function_node("assemble", assemble_project)
        .add_function_node("generate_deck", generate_deck)
        .add_function_node("finalize", finalize)
        .set_entry("assemble")
        .add_edge("assemble", "generate_deck")
        .add_edge("generate_deck", "finalize")
        .add_terminal("finalize")
        .build()
    )

    return graph


deck_only_graph = build_deck_only_graph()
